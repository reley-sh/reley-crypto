/**
 * Validate an X25519 public key. Rejects wrong-length and low-order points.
 */
export function validateX25519PublicKey(pk: Uint8Array): boolean {
  if (!pk || pk.length !== 32) return false;
  for (const lop of LOW_ORDER_POINTS) {
    let diff = 0;
    for (let i = 0; i < 32; i++) diff |= pk[i] ^ lop[i];
    if (diff === 0) return false;
  }
  return true;
}

/**
 * Compute X25519 ECDH shared secret from our secret key and their public key.
 * Returns 32-byte raw shared secret.
 *
 * Security (C-3): rejects known low-order public keys that would produce
 * a predictable shared secret, preventing small-subgroup attacks.
 */
export async function computeSharedSecret(
  ourSecretKey: Uint8Array,
  theirPublicKey: Uint8Array,
): Promise<Uint8Array> {
  if (ourSecretKey.length !== 32) {
    throw new Error(`Secret key must be 32 bytes, got ${ourSecretKey.length}`);
  }
  if (theirPublicKey.length !== 32) {
    throw new Error(`Public key must be 32 bytes, got ${theirPublicKey.length}`);
  }
  if (!validateX25519PublicKey(theirPublicKey)) {
    throw new Error('Invalid public key: low-order point rejected');
  }
  const s = await ensureSodium();
  return s.crypto_scalarmult(ourSecretKey, theirPublicKey);
}
